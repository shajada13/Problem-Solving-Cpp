#include <iostream>
using namespace std;

int main() {

    int num = 34;  // Declare an integer variable 'num' and assign it the value 34

    cout << "Number value : " << num << endl;  // Prints the value of 'num' and moves to a new line using endl

    return 0;
}

// Note:
// int is a data type used to store whole numbers (integers).
// It typically uses 4 bytes (32 bits) of memory, and its range is from -2,147,483,648 to 2,147,483,647.
// The 'endl' here creates a new line and flushes the output buffer, ensuring that the output is displayed immediately.

// বাংলায়:
// এখানে 'int' ডেটা টাইপ ব্যবহার করা হয়েছে, যা সাধারণত ৪ বাইট (৩২ বিট) মেমরি ব্যবহার করে।
// এর মানের সীমা হলো -২,১৪৭,৪৮৩,৬৪৮ থেকে ২,১৪৭,৪৮৩,৬৪৭ পর্যন্ত।
// 'endl' নতুন লাইন তৈরি করে এবং আউটপুট বাফার ফ্লাশ করে, যাতে আউটপুটটি তৎক্ষণাৎ প্রদর্শিত হয়।
