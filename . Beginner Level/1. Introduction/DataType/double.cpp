#include <iostream>
using namespace std;

int main() {

    double num = 3.1415926535;  // একটি ডাবল টাইপ ভেরিয়েবল 'num' ঘোষণা করা হলো এবং তাকে ৩.১৪১৫৯২৬৫৩৫ মান দেওয়া হলো
    cout << "Double value: " << num << endl;  // 'num' এর মান প্রিন্ট করা হবে

    return 0;
}

// Note (English):
// 'double' is a data type that can store decimal (fractional) numbers with higher precision than 'float'.
// It typically uses 8 bytes (64 bits) of memory and maintains precision up to about 15–16 digits.
// A 'double' is used when more accuracy is needed for calculations involving decimal numbers.

// Note (বাংলায়):
// 'double' ডেটা টাইপ দিয়ে দশমিক সংখ্যা আরো বেশি নির্ভুলতার সাথে সংরক্ষণ করা হয়।
// এটি সাধারণত ৮ বাইট মেমরি ব্যবহার করে এবং প্রায় ১৫-১৬ ডিজিট পর্যন্ত সঠিক মান প্রদান করে।
// বেশি নির্ভুলতা প্রয়োজন হলে 'double' ভেরিয়েবল ব্যবহার করা হয়।
