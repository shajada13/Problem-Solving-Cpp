#include <iostream>
using namespace std;

int main() {

    char ch = 'a';  // Declare a character variable 'ch' and assign it the value 'a'
    cout << ch << endl;  // Prints the value of 'ch' and moves to a new line using endl

    return 0;
}

// Note:
// 'char' is used to store a single character, typically represented by a single byte (8 bits) of memory.
// The range of 'char' is usually from -128 to 127 (for signed char) or 0 to 255 (for unsigned char).

// বাংলায়:
// 'char' ডেটা টাইপ একটি একক অক্ষর সংরক্ষণ করতে ব্যবহৃত হয়। এটি সাধারণত ১ বাইট (৮ বিট) মেমরি ব্যবহার করে।
// 'char' এর মানের সীমা সাধারণত -১২৮ থেকে ১২৭ (signed char) অথবা ০ থেকে ২৫৫ (unsigned char)।
